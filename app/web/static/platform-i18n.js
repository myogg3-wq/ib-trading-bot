function resolveGeneratedLanguage(lang) {
  const normalized = normalizeLanguageCode(lang);
  return GENERATED_COPY_TEMPLATES[normalized]
    ? normalized
    : GENERATED_COPY_TEMPLATES[normalized.split("-")[0]]
      ? normalized.split("-")[0]
      : "en";
}

function formatGenerated(template, replacements = {}) {
  return String(template || "").replace(/\{(\w+)\}/g, (_, key) => `${replacements[key] ?? ""}`);
}

function metricFromMap(record, labels = []) {
  const priceMap = Array.isArray(record?.price_map) ? record.price_map : [];
  for (const label of labels) {
    const value = priceMap.find((item) => item?.label === label)?.value;
    if (value) {
      return value;
    }
  }
  return "--";
}

export function buildGeneratedThreadCopy(record, lang) {
  if (!record?.kind) {
    return null;
  }

  const localeKey = resolveGeneratedLanguage(lang);
  const templates = GENERATED_COPY_TEMPLATES[localeKey]?.thread?.[record.kind] || GENERATED_COPY_TEMPLATES.en.thread[record.kind];
  if (!templates) {
    return null;
  }

  const entry = metricFromMap(record, ["Entry", "Watch", "Trigger", "Focus"]);
  const risk = metricFromMap(record, ["Risk", "Watch", "Trigger", "Focus"]);
  const focus = metricFromMap(record, ["Focus", "Trigger", "Entry"]);
  const trigger = metricFromMap(record, ["Trigger", "Watch", "Entry"]);
  const exit = metricFromMap(record, ["Exit", "Entry"]);
  const returnValue = metricFromMap(record, ["Return"]);
  const hold = metricFromMap(record, ["Hold"]);
  const values = {
    ticker: record.ticker || "",
    company: record.company || record.ticker || "",
    entry,
    risk,
    focus,
    trigger,
    exit,
    returnValue,
    hold,
  };

  return {
    headline: formatGenerated(templates.headline, values),
    summary: formatGenerated(templates.summary, values),
    beats: [formatGenerated(templates.beat, values)],
    footer: formatGenerated(templates.footer, values),
  };
}

export function buildGeneratedWatchCopy(record, lang) {
  if (!record?.ticker || !record?.trigger) {
    return null;
  }

  const localeKey = resolveGeneratedLanguage(lang);
  const templates = GENERATED_COPY_TEMPLATES[localeKey]?.watch || GENERATED_COPY_TEMPLATES.en.watch;
  const values = {
    ticker: record.ticker || "",
    company: record.company || record.ticker || "",
    trigger: record.trigger || "--",
  };

  return {
    headline: formatGenerated(templates.headline, values),
    note: formatGenerated(templates.note, values),
  };
}

