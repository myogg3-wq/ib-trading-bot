function normalizeLanguageCode(raw) {
  if (!raw) {
    return "en";
  }

  const lower = raw.toLowerCase();
  if (lower.startsWith("zh")) {
    return "zh-CN";
  }

  const exact = SUPPORTED_LANGUAGES.find((item) => item.code.toLowerCase() === lower);
  if (exact) {
    return exact.code;
  }

  const base = lower.split("-")[0];
  const prefix = SUPPORTED_LANGUAGES.find((item) => item.code.toLowerCase() === base);
  return prefix ? prefix.code : "en";
}

function mergeObject(base = {}, override = {}) {
  return { ...base, ...(override || {}) };
}

function mergeTuple(baseItem, tuple, keys) {
  if (!tuple) {
    return { ...baseItem };
  }

  const localized = { ...baseItem };
  keys.forEach((key, index) => {
    if (tuple[index]) {
      localized[key] = tuple[index];
    }
  });
  return localized;
}

export function getPreferredLanguage() {
  const queryLang = new URLSearchParams(window.location.search).get("lang");
  if (queryLang) {
    return normalizeLanguageCode(queryLang);
  }

  const stored = window.localStorage.getItem("platform-language");
  if (stored) {
    return normalizeLanguageCode(stored);
  }

  const serverProvided = document.documentElement?.dataset?.platformDefaultLanguage;
  if (serverProvided) {
    return normalizeLanguageCode(serverProvided);
  }

  const candidates = [...(navigator.languages || []), navigator.language].filter(Boolean);
  for (const candidate of candidates) {
    const normalized = normalizeLanguageCode(candidate);
    if (SUPPORTED_LANGUAGES.some((item) => item.code === normalized)) {
      return normalized;
    }
  }

  return "en";
}

export function isRtlLanguage(lang) {
  return RTL_LANGUAGES.has(lang);
}

export function taxonomyLabel(lang, group, value) {
  const locale = LOCALES[lang] || {};
  return locale.taxonomy?.[group]?.[value] || BASE_TAXONOMY[group]?.[value] || value;
}

export function buildLocalizedBlueprint(baseBlueprint, lang) {
  const normalized = normalizeLanguageCode(lang);
  const locale = LOCALES[normalized] || {};
  const ui = mergeObject(BASE_UI, locale.ui);

  return {
    ...baseBlueprint,
    language: normalized,
    ui,
    meta: {
      ...baseBlueprint.meta,
      ...(locale.meta || {}),
    },
    postures: Object.fromEntries(
      Object.entries(baseBlueprint.postures).map(([key, posture]) => {
        const tuple = locale.postures?.[key];
        return [
          key,
          tuple ? { ...posture, label: tuple[0], tone: tuple[1] } : { ...posture },
        ];
      })
    ),
    modules: baseBlueprint.modules.map((module) =>
      mergeTuple(module, locale.modules?.[module.id], ["name", "summary", "focus"])
    ),
    presets: baseBlueprint.presets.map((preset) =>
      mergeTuple(preset, locale.presets?.[preset.id], ["name", "summary"])
    ),
    guardrails: baseBlueprint.guardrails.map((guardrail, index) =>
      mergeTuple(guardrail, locale.guardrails?.[index], ["title", "detail"])
    ),
    phases: baseBlueprint.phases.map((phase, index) =>
      mergeTuple(phase, locale.phases?.[index], ["title", "summary"])
    ),
    ideas: baseBlueprint.ideas.map((idea) =>
      mergeTuple(idea, locale.ideas?.[idea.id], ["title", "summary", "compliance"])
    ),
  };
}

